void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	new_number;

	i = 0;
	while (i < size)
	{
		j = i + 1;
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				new_number = tab[i];
				tab[i] = tab[j];
				tab[j] = new_number;
			}
			j++;
		}
		i++;
	}
}
